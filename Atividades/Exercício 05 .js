/* Crie um programa que declare duas variáveis e exiba o resultado da soma,
subtração, multiplicação e divisão desses números.
*/

let n1 = 10
let n2 = 3

console.log(`${n1} + ${n2} = ` + (n1 + n2));
console.log(`${n1} - ${n2} = ` + (n1 - n2));
console.log(`${n1} * ${n2} = ` + (n1 * n2));
console.log(`${n1} / ${n2} = ` + (n1 / n2).toFixed(3));

